export function holonTypeNameFromEnum(value: number | string | undefined | null): string {
  if (value === undefined || value === null) return 'Holon';
  if (typeof value === 'string') return value;
  if (!Number.isFinite(value) || value < 0 || value >= HOLON_TYPE_ENUM_NAMES.length)
    return `HolonType ${value}`;
  return HOLON_TYPE_ENUM_NAMES[value];
}
