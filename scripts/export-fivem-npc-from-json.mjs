/**
 * Reads npcs/<name>.json (STAR + ElevenLabs IDs) and writes a FiveM resource folder
 * you can drop into the server's resources directory. No OASIS knowledge required on the player side.
 *
 * Usage:
 *   node scripts/export-fivem-npc-from-json.mjs [path/to/npc.json] [outputDir]
 *
 * Defaults (from OASIS-IDE repo layout):
 *   json:   <repo>/npcs/martinez.json or OASIS-IDE/npcs/martinez.json
 *   output: <repo>/fivem-resources/MartinezOasisNpc (derived from filename)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ideRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(ideRoot, '..');

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

function resolveIds(raw) {
  const star =
    raw.starHolonId ||
    raw.star_holon_id ||
    raw.STARHolonId ||
    raw.data?.starHolonId;
  const agent =
    raw.elevenLabsAgentId ||
    raw.elevenlabs_agent_id ||
    raw.ElevenLabsAgentId ||
    raw.data?.elevenLabsAgentId;
  const voice =
    raw.elevenLabsVoiceId || raw.elevenlabs_voice_id || raw.voice_id || '';
  const displayName = raw.name || raw.displayName || 'NPC';
  return { star, agent, voice, displayName };
}

function findDefaultJson() {
  const candidates = [
    path.join(repoRoot, 'npcs', 'martinez.json'),
    path.join(ideRoot, 'npcs', 'martinez.json'),
    path.join(process.cwd(), 'npcs', 'martinez.json'),
  ];
  for (const c of candidates) {
    if (fs.existsSync(c)) return c;
  }
  return null;
}

const jsonPath = process.argv[2] || findDefaultJson();
if (!jsonPath || !fs.existsSync(jsonPath)) {
  console.error('Usage: node export-fivem-npc-from-json.mjs <npc.json> [outputDir]');
  console.error('Or place npcs/martinez.json at repo root or under OASIS-IDE/npcs/.');
  process.exit(1);
}

const baseName = path.basename(jsonPath, '.json');
const titleFromFile = baseName.charAt(0).toUpperCase() + baseName.slice(1);
const resourceName = `${titleFromFile}OasisNpc`;
const outRoot =
  process.argv[3] || path.join(repoRoot, 'fivem-resources', resourceName);

const raw = readJson(jsonPath);
const ids = resolveIds(raw);
const displayName = ids.displayName === 'NPC' ? titleFromFile : ids.displayName;
const { star, agent, voice } = ids;
if (!star || !agent) {
  console.error('JSON must include star holon id and ElevenLabs agent id (camelCase or snake_case).');
  process.exit(1);
}

fs.mkdirSync(outRoot, { recursive: true });

const safeLua = (s) => String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");

const configLua = `-- Auto-generated by OASIS IDE export. Do not commit secrets here (IDs are fine).
-- Other resources can read: MartinezOasisNpcConfig.StarHolonId etc. (global table name matches export)

${resourceName}Config = {
    DisplayName = '${safeLua(displayName)}',
    StarHolonId = '${safeLua(star)}',
    ElevenLabsAgentId = '${safeLua(agent)}',
    ElevenLabsVoiceId = '${safeLua(voice)}',
}
`;

const fxmanifest = `fx_version 'cerulean'
game 'gta5'

name '${resourceName}'
author 'OASIS IDE'
description 'OASIS STAR holon + ElevenLabs agent IDs for ${displayName} (exported drop-in)'
version '1.0.0'

shared_script 'config.lua'
`;

const readme = `OASIS drop-in (no coding required)
================================

This folder is a minimal FiveM resource. It only holds your OASIS and ElevenLabs IDs so other scripts can use them.

Do this once on your server machine:

  1) Copy this entire folder into your server's resources directory
     (same level as other resources, often server-data/resources/).

  2) Open server.cfg (or your hosting panel's server config) and add ONE line:

     ensure ${resourceName}

  3) Restart the server (or start the resource from txAdmin).

Your IDs are in config.lua. Framework devs can reference the global table ${resourceName}Config in Lua.

Voice in-game: hook ElevenLabs Conversational AI with ElevenLabsAgentId (usually via your server's backend or NUI, not inside this stub). The holon id is for OASIS quest and persistence when you connect ONODE.

Generated from: ${path.basename(jsonPath)}
`;

fs.writeFileSync(path.join(outRoot, 'config.lua'), configLua, 'utf8');
fs.writeFileSync(path.join(outRoot, 'fxmanifest.lua'), fxmanifest, 'utf8');
fs.writeFileSync(path.join(outRoot, 'README.txt'), readme, 'utf8');

console.log('Wrote FiveM resource:', outRoot);
console.log('Next: copy folder to resources, add ensure ' + resourceName + ' to server.cfg');
